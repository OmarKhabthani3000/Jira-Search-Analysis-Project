package example;

public class Cat
{
    private String id;
    private String name;

    public Cat()
    {
    }
    
    public String getId() {
        return id;
    }

    private void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
