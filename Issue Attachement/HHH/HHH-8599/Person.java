package com.company.product.domain;

public class Person
{
    private Long id;

    private String firstname;

    public Long getId()
    {
        return id;
    }

    public void setId(Long anId)
    {
        id = anId;
    }

    public String getFirstname()
    {
        return firstname;
    }

    public void setFirstname(String aFirstname)
    {
        firstname = aFirstname;
    }
}
